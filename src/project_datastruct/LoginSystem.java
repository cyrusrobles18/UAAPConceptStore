    private String getFullName() {
        String fullName = "";
        try {
            BufferedReader br = new BufferedReader(new FileReader(this.filepathAcc));
            String tempLine;
            String tempUsername;
            String[] acc;

            while ((tempLine = br.readLine()) != null) {
                acc = tempLine.split(",");
                tempUsername = acc[0];

                if (tempUsername.trim().equals(tfUsername.getText())) {
                    fullName = acc[3];
                    break;

                }
            }
            br.close();
        } catch (Exception e) {
            System.out.println("");
        }

        return fullName;
    }


