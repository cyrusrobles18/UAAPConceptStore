    private void BtnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSignUpActionPerformed
        this.userType = "COSTUMER";
        if (fNameAlert.getText().equals("Valid") && lNameAlert.getText().equals("Valid")
                && userAlert.getText().equals("Valid") && passAlert.getText().equals("Valid")
                && univAlert.getText().equals("Valid") && courseAlert.getText().equals("Valid")
                && IDnumAlert.getText().equals("Valid")) {
            if (!verifyLogin(tfUsername.getText().trim(), jpSignin.getText().trim(), userType.trim())) {
                if (!tfUsername.getText().equals(jpSignin.getText())
                        && (jpSignin.getText().length() > 4 || jpSignin.getText().length() < 16)) {
                    createAcc(tfUser.getText().trim(), jpSignUp.getText().trim(), userType.trim());
                    JOptionPane.showMessageDialog(this, "Signup Successful");
                    fNameAlert.setText("");
                    lNameAlert.setText("");
                    userAlert.setText("");
                    passAlert.setText("");
                    univAlert.setText("");
                    courseAlert.setText("");
                    IDnumAlert.setText("");
                    tfFirstname.setText("");
                    tfLastname.setText("");
                    tfUser.setText("");
                    jpSignUp.setText("");
                    tfUniversity.setText("");
                    tfCourse.setText("");
                    tfIDnum.setText("");
                }
            }
        } else {
            if (fNameAlert.getText().equals("*") || lNameAlert.getText().equals("*")
                    || userAlert.getText().equals("*") || passAlert.getText().equals("*")
                    || univAlert.getText().equals("*") || courseAlert.getText().equals("*")
                    || IDnumAlert.getText().equals("*")) {
                JOptionPane.showMessageDialog(this, "Fillup all the requirements!", " Alert! ", JOptionPane.WARNING_MESSAGE);
            }
            JOptionPane.showMessageDialog(this, "Sign up failed!", " Alert! ", JOptionPane.WARNING_MESSAGE);
        }
        if (false) {
            createAcc(tfUser.getText().trim(), jpSignUp.getText().trim(), userType.trim());
        }
    }//GEN-LAST:event_BtnSignUpActionPerformed

