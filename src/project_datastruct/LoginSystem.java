    private boolean verifyLogin(String username, String password, String type) {
        boolean found = false;
        try {
            BufferedReader br = new BufferedReader(new FileReader(this.filepathAcc));
            String tempLine;
            String tempUsername;
            String tempPassword;
            String tempType;
            String[] acc;

            while ((tempLine = br.readLine()) != null) {
                acc = tempLine.split(",");
                tempUsername = acc[0];
                tempPassword = acc[1];
                tempType = acc[2];

                if (tempUsername.trim().equals(username.trim()) && tempPassword.trim().trim().equals(password.trim()) && tempType.trim().equals(type.trim())) {
                    found = true;
                    break;

                }
            }
            br.close();
        } catch (Exception e) {
            System.out.println("Verify Login Error");
        }

        return found;
    }

